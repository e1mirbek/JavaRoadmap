        // –û–ø–µ—Ä–∞—Ç–æ—Ä || —Å—Ä–∞–±–æ—Ç–∞–µ—Ç, –µ—Å–ª–∏ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ true.
