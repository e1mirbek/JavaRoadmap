package udemy.course.lesson21;

// if - then : стейтмент

public class TwentyFirstLs {
    public static void main(String[] args) {

        int delimoe = 5;
        int delitel = 0;
        if (delitel != 0) {
            // выражение
            int result = delimoe / delitel;
            System.out.println(result);
        } else {
            System.out.println("Ошибка delitel равен 0");
        }
    }
}
