        //üìå –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –ø—É—Ç—å "C:/folder/example.txt" ‚Üí getName() –≤–µ—Ä–Ω—ë—Ç "example.txt".
